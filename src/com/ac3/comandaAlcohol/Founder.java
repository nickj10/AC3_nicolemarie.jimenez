package com.ac3.comandaAlcohol;

/**
 * Representa el fundador d'un alcohol.
 * @author Nicole Marie Jimenez
 */
public class Founder {
    private String name;
    private int born;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getBorn() {
        return born;
    }

    public void setBorn(int born) {
        this.born = born;
    }
}
